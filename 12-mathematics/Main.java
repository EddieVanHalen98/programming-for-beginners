public class Main {

	public static void main(String[] args) {
		// Square root
		double x = Math.sqrt(4);
		System.out.println(x);
		
		// Exponentials
		x = Math.sqrt(Math.pow(2, 2));
		System.out.println(x);
		
		// Trigonometry
		double y = Math.sin(Math.toRadians(90));
		System.out.println(y);
		
		// Rounding
		System.out.println(Math.round(12.4657));
		System.out.println(Math.round(12.5657));
	}
	
}