public class Main {

	public static void main(String[] args) {
		//  Basic arithmetic operators
		int a = 5 + 3;
		int b = (5 * 8) / 10;
		int c = a - b;
		
		System.out.println(c);
		
		// Modulus
		int i = c % 2;
		
		System.out.println(i);
		
		// Changing values
		a = a + 2;
		a += 2;
		a++;
		a++;
		
		System.out.println(a);
		
		// Integer division
		System.out.println(60 / 75);
		System.out.println(149 / 75);
	}
	
}